package Chapter_02;

public class Multi_If_Condition
{
    public static void main(String[] args)
    {
        int i = 2;
        if(i == 0)
        {
            System.out.println("i 변수의 값은 0입니다.");
        }
        else if(i == 1)
        {
            System.out.println("i 변수의 값은 1입니다.");
        }
        else if(i == 2)
        {
            System.out.println("i 변수의 값은 2입니다.");
        }
        System.out.println("감사합니다. 프로그램을 종료합니다.");
    }
}
