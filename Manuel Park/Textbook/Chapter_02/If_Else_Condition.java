package Chapter_02;

public class If_Else_Condition
{
    public static void main(String[] args)
    {
        int i = 1;
        if(i == 0)
        {
            System.out.println("i 변수의 값은 0입니다.");
        }
        else
        {
            System.out.println("i 변수의 값은 0이 아닙니다.");
        }
        System.out.println("감사합니다. 프로그램을 종료합니다.");
    }
}
